const fs = require('fs')
const path = require('path')

const { CHECK_GUIDANCE } = require('./create-windows-smoke-runbook')
const { REQUIRED_CHECKS, validateReport } = require('./validate-windows-smoke-report')

const DEFAULT_OUTPUT_NAME = 'windows-smoke-collector.ps1'

const usage = () => [
  'Usage: node scripts/create-windows-smoke-collector.js <report.json> [--output <collector.ps1>]',
  '',
  'Creates a PowerShell helper for collecting Windows smoke evidence during real validation.',
  'The collector does not mark any smoke check as passed.'
].join('\n')

const parseArgs = (argv) => {
  const options = {
    reportPath: null,
    outputPath: null,
    help: false
  }

  const readValue = (index, flag) => {
    const value = argv[index + 1]
    if (!value || value.startsWith('--')) throw new Error(`${flag} requires a value`)
    return value
  }

  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index]
    if (arg === '--help' || arg === '-h') {
      options.help = true
    } else if (arg === '--output') {
      options.outputPath = readValue(index, arg)
      index += 1
    } else if (!options.reportPath) {
      options.reportPath = arg
    } else {
      throw new Error(`Unexpected argument: ${arg}`)
    }
  }

  return options
}

const loadReport = (reportPath, fsImpl = fs) => {
  if (!reportPath) throw new Error('Report path is required')
  const absolutePath = path.resolve(reportPath)
  return {
    absolutePath,
    report: JSON.parse(fsImpl.readFileSync(absolutePath, 'utf-8'))
  }
}

const defaultOutputPath = (reportPath) => path.join(path.dirname(path.resolve(reportPath)), DEFAULT_OUTPUT_NAME)

const psSingleQuote = (value) => `'${String(value).replace(/'/g, "''")}'`

const markdownTableEscape = (value) => String(value || '').replace(/\|/g, '\\|').replace(/\r?\n/g, ' ')

const createManualChecklist = () => {
  const lines = []
  lines.push('# OpenPet Windows Smoke Manual Checklist')
  lines.push('')
  lines.push('This checklist is generated from the same required check matrix used by the JSON validator. Attach concrete evidence before marking any check as pass.')
  lines.push('')
  lines.push('| Check ID | What To Prove | Evidence Guidance |')
  lines.push('|----------|---------------|-------------------|')
  for (const check of REQUIRED_CHECKS) {
    lines.push(`| \`${check.id}\` | ${markdownTableEscape(check.label)} | ${markdownTableEscape(CHECK_GUIDANCE[check.id] || 'Record concrete evidence from the Windows validation run.')} |`)
  }
  lines.push('')
  return lines.join('\n')
}

const createCommandNotes = ({ reportFileName }) => {
  const lines = []
  lines.push('# OpenPet Windows Smoke Report Update Commands')
  lines.push('')
  lines.push('Run these from the repository root after collecting evidence on Windows. Replace placeholders with paths or excerpts from the files generated by windows-smoke-collector.ps1.')
  lines.push('')
  lines.push('```powershell')
  lines.push(`npm run update-windows-smoke-report -- ${reportFileName} --set-env windowsVersion="<copy from windows-smoke-evidence/environment.txt>" --set-env machine="<clean Windows machine name>" --set-env runner="manual Windows smoke validation" --set-env evidence="<evidence directory or transcript link>"`)
  lines.push(`npm run update-windows-smoke-report -- ${reportFileName} --set-artifact authenticodeEvidence="<copy from windows-smoke-evidence/authenticode.txt>"`)
  lines.push(`npm run validate-windows-smoke-report -- ${reportFileName} --allow-pending`)
  lines.push('```')
  lines.push('')
  lines.push('Do not use these commands to mark checks as pass until the matching manual validation evidence exists.')
  lines.push('')
  return lines.join('\n')
}

const hereString = (value) => `@'\n${String(value).replace(/\r\n/g, '\n').trimEnd()}\n'@`

const createCollector = ({ report, reportPath, generatedAt = new Date() }) => {
  const validation = validateReport(report, { allowPending: true })
  if (!validation.ok) {
    throw new Error(`Cannot create Windows smoke collector from an invalid report: ${validation.errors.join('; ')}`)
  }

  const reportFileName = path.basename(reportPath || 'windows-smoke-report.json')
  const manualChecklist = createManualChecklist()
  const commandNotes = createCommandNotes({ reportFileName })
  const lines = []

  lines.push('<#')
  lines.push('.SYNOPSIS')
  lines.push('Collects local Windows evidence for an OpenPet smoke validation session.')
  lines.push('')
  lines.push('.DESCRIPTION')
  lines.push('This helper records environment, Authenticode, process, and installation snapshots.')
  lines.push('It does not mark any Windows smoke check as passed and does not prove release readiness by itself.')
  lines.push(`Generated: ${generatedAt.toISOString()}`)
  lines.push('#>')
  lines.push('param(')
  lines.push(`  [string]$ReportPath = (Join-Path $PSScriptRoot ${psSingleQuote(reportFileName)}),`)
  lines.push("  [string]$EvidenceDir = (Join-Path $PSScriptRoot 'windows-smoke-evidence'),")
  lines.push("  [string]$InstallerPath = '',")
  lines.push("  [string]$AppProcessName = 'OpenPet'")
  lines.push(')')
  lines.push('')
  lines.push("$ErrorActionPreference = 'Stop'")
  lines.push('')
  lines.push('function Write-EvidenceFile {')
  lines.push('  param(')
  lines.push('    [Parameter(Mandatory = $true)][string]$Name,')
  lines.push('    [Parameter(Mandatory = $true)][string]$Content')
  lines.push('  )')
  lines.push('')
  lines.push('  $path = Join-Path $EvidenceDir $Name')
  lines.push('  Set-Content -LiteralPath $path -Value $Content.TrimEnd() -Encoding UTF8')
  lines.push('  return $path')
  lines.push('}')
  lines.push('')
  lines.push('New-Item -ItemType Directory -Force -Path $EvidenceDir | Out-Null')
  lines.push('$collectedAt = (Get-Date).ToString(\'o\')')
  lines.push('$osInfo = $null')
  lines.push('try {')
  lines.push('  $osInfo = Get-CimInstance Win32_OperatingSystem')
  lines.push('} catch {')
  lines.push('  Write-Warning "Unable to query Win32_OperatingSystem: $($_.Exception.Message)"')
  lines.push('}')
  lines.push('')
  lines.push('$environmentLines = @(')
  lines.push('  "CollectedAt: $collectedAt"')
  lines.push('  "ComputerName: $env:COMPUTERNAME"')
  lines.push('  "UserName: $env:USERNAME"')
  lines.push('  "PowerShell: $($PSVersionTable.PSVersion)"')
  lines.push('  "ReportPath: $ReportPath"')
  lines.push('  "EvidenceDir: $EvidenceDir"')
  lines.push(')')
  lines.push('if ($osInfo) {')
  lines.push('  $environmentLines += "WindowsCaption: $($osInfo.Caption)"')
  lines.push('  $environmentLines += "WindowsVersion: $($osInfo.Version)"')
  lines.push('  $environmentLines += "WindowsBuild: $($osInfo.BuildNumber)"')
  lines.push('  $environmentLines += "OSArchitecture: $($osInfo.OSArchitecture)"')
  lines.push('}')
  lines.push('Write-EvidenceFile -Name \'environment.txt\' -Content ($environmentLines -join [Environment]::NewLine) | Out-Null')
  lines.push('')
  lines.push('$report = $null')
  lines.push('if (Test-Path -LiteralPath $ReportPath) {')
  lines.push('  try {')
  lines.push('    $report = Get-Content -LiteralPath $ReportPath -Raw | ConvertFrom-Json')
  lines.push('  } catch {')
  lines.push('    Write-Warning "Unable to read smoke report JSON: $($_.Exception.Message)"')
  lines.push('  }')
  lines.push('} else {')
  lines.push('  Write-Warning "Smoke report not found: $ReportPath"')
  lines.push('}')
  lines.push('')
  lines.push('if (-not $InstallerPath -and $report -and $report.artifact -and $report.artifact.installer) {')
  lines.push('  $candidate = Join-Path (Split-Path -Parent $ReportPath) $report.artifact.installer')
  lines.push('  if (Test-Path -LiteralPath $candidate) {')
  lines.push('    $InstallerPath = $candidate')
  lines.push('  }')
  lines.push('}')
  lines.push('')
  lines.push('if ($InstallerPath -and (Test-Path -LiteralPath $InstallerPath)) {')
  lines.push('  try {')
  lines.push('    $signature = Get-AuthenticodeSignature -LiteralPath $InstallerPath | Format-List * | Out-String -Width 240')
  lines.push('    Write-EvidenceFile -Name \'authenticode.txt\' -Content $signature | Out-Null')
  lines.push('  } catch {')
  lines.push('    Write-EvidenceFile -Name \'authenticode.txt\' -Content "Get-AuthenticodeSignature failed: $($_.Exception.Message)" | Out-Null')
  lines.push('  }')
  lines.push('} else {')
  lines.push('  Write-EvidenceFile -Name \'authenticode.txt\' -Content "Installer not found. Pass -InstallerPath or place the installer next to $ReportPath." | Out-Null')
  lines.push('}')
  lines.push('')
  lines.push('try {')
  lines.push('  $processSnapshot = Get-Process -Name $AppProcessName -ErrorAction SilentlyContinue | Select-Object Name, Id, Path, StartTime, MainWindowTitle | Format-List | Out-String -Width 240')
  lines.push('  if (-not $processSnapshot.Trim()) { $processSnapshot = "No process named $AppProcessName was running at collection time." }')
  lines.push('  Write-EvidenceFile -Name \'process.txt\' -Content $processSnapshot | Out-Null')
  lines.push('} catch {')
  lines.push('  Write-EvidenceFile -Name \'process.txt\' -Content "Process snapshot failed: $($_.Exception.Message)" | Out-Null')
  lines.push('}')
  lines.push('')
  lines.push('$uninstallRoots = @(')
  lines.push("  'HKCU:\\Software\\Microsoft\\Windows\\CurrentVersion\\Uninstall',")
  lines.push("  'HKLM:\\Software\\Microsoft\\Windows\\CurrentVersion\\Uninstall'")
  lines.push(')')
  lines.push('$installMatches = foreach ($root in $uninstallRoots) {')
  lines.push('  if (Test-Path -LiteralPath $root) {')
  lines.push('    Get-ChildItem -LiteralPath $root -ErrorAction SilentlyContinue | ForEach-Object {')
  lines.push('      try {')
  lines.push('        Get-ItemProperty -LiteralPath $_.PSPath')
  lines.push('      } catch {')
  lines.push('        Write-Warning "Unable to read uninstall entry under ${root}: $($_.Exception.Message)"')
  lines.push('      }')
  lines.push('    } | Where-Object { $_.DisplayName -like \'*OpenPet*\' } | Select-Object DisplayName, DisplayVersion, Publisher, InstallLocation, UninstallString')
  lines.push('  }')
  lines.push('}')
  lines.push('$installSnapshot = $installMatches | Format-List | Out-String -Width 240')
  lines.push('if (-not $installSnapshot.Trim()) { $installSnapshot = \'No OpenPet uninstall entry was found at collection time.\' }')
  lines.push('Write-EvidenceFile -Name \'install-registry.txt\' -Content $installSnapshot | Out-Null')
  lines.push('')
  lines.push(`$manualChecklist = ${hereString(manualChecklist)}`)
  lines.push("Write-EvidenceFile -Name 'manual-checks.md' -Content $manualChecklist | Out-Null")
  lines.push('')
  lines.push(`$commandNotes = ${hereString(commandNotes)}`)
  lines.push("Write-EvidenceFile -Name 'update-report-commands.md' -Content $commandNotes | Out-Null")
  lines.push('')
  lines.push('Write-Host "OpenPet Windows smoke evidence collected in: $EvidenceDir"')
  lines.push('Write-Host "Review manual-checks.md and attach concrete evidence before marking any check as pass."')
  lines.push('')

  return lines.join('\n')
}

const writeCollector = ({ content, outputPath, fsImpl = fs }) => {
  const absoluteOutputPath = path.resolve(outputPath)
  fsImpl.mkdirSync(path.dirname(absoluteOutputPath), { recursive: true })
  fsImpl.writeFileSync(absoluteOutputPath, `${content.trimEnd()}\n`)
  return absoluteOutputPath
}

const main = () => {
  const options = parseArgs(process.argv.slice(2))
  if (options.help) {
    console.log(usage())
    return
  }

  const { absolutePath, report } = loadReport(options.reportPath)
  const outputPath = options.outputPath || defaultOutputPath(absolutePath)
  const content = createCollector({ report, reportPath: absolutePath })
  const writtenPath = writeCollector({ content, outputPath })

  console.log(`Windows smoke collector created: ${writtenPath}`)
  console.log(`Checks referenced: ${REQUIRED_CHECKS.length}`)
  console.log('This collector gathers evidence only; it does not prove Windows smoke validation passed.')
}

if (require.main === module) {
  try {
    main()
  } catch (err) {
    console.error(err.message || err)
    process.exit(1)
  }
}

module.exports = {
  createCollector,
  createCommandNotes,
  createManualChecklist,
  defaultOutputPath,
  parseArgs,
  writeCollector
}
